package com.carl.pongspiel.client.ui;

public class Position {
	private int top = 0;
	private int left = 0;
	
	public Position(int top, int left){
		this.top = top;
		this.left = left;
	}

	public int getTop() {
		return top;
	}

	public void setTop(int top) {
		this.top = top;
	}

	public int getLeft() {
		return left;
	}

	public void setLeft(int left) {
		this.left = left;
	}
	
	

}
